package ex1;

/**
 * Cette classe fournit des opérations arithmétiques de base pour l'addition et la soustraction.
 */
public class Calcul {

	/**
	 * Additionne deux entiers et retourne le résultat.
	 *
	 * @param a le premier entier à additionner
	 * @param b le deuxième entier à additionner
	 * @return la somme de a et b
	 */
	public int additionner(int a, int b) {
		return a + b;
	}

	/**
	 * Soustrait le deuxième entier du premier et retourne le résultat.
	 *
	 * @param a l'entier de départ
	 * @param b l'entier à soustraire
	 * @return la différence entre a et b
	 */
	public int soustraire(int a, int b) {
		return a - b;
	}
}
